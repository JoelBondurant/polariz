use iced::Color;

pub fn viridis(t: f32) -> Color {
	let r = 0.184455
		+ t * (0.107708
			+ t * (-0.327241 + t * (-4.599932 + t * (6.203736 + t * (4.751787 + t * -5.432077)))));
	let g = 0.005768
		+ t * (1.39647
			+ t * (0.214814 + t * (-5.758238 + t * (14.153965 + t * (-13.749439 + t * 4.641571)))));
	let b = 0.267511
		+ t * (0.073383
			+ t * (15.657724
				+ t * (-90.25783 + t * (202.56079 + t * (-202.6031 + t * 74.394908)))));
	Color::from_rgb(r, g, b)
}
